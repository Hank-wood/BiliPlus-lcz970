<div class="framesubtitle">BiliPlus能够为我做什么？</div>
<div class="framedescription">BiliPlus可以使您：<br/>·方便地下载哔哩哔哩上的任意视频/弹幕文件<br/>·选择您喜欢的弹幕播放器播放弹幕视频<br/>·通过简洁的页面无干扰地欣赏弹幕视频<br/>·快速搜索哔哩哔哩上的投稿/专题并直接下载/播放</div>
<br/>
<hr>
<div class="framesubtitle">为什么我总是遇到错误？</div>
<div class="framedescription">由于BiliPlus通过哔哩哔哩开放平台获取信息，因此BiliPlus的稳定性依赖于哔哩哔哩开放平台。<br/>以下是对于一些常见错误代码的解释，您可以参照这些解释明白错误原因。<br/><b>[-403] no perm error</b> 无投稿访问权限，可能是B站已经屏蔽或删除该投稿。<br/><b>[-404] no such doc</b> 文档不存在。请确定您输入了正确的AV号。<br/><b>[视频地址403、404]</b> 视频链接地址已失效，请点击页面下方的“刷新数据”刷新缓存数据。</div>
<br/>
<hr>
<div class="framesubtitle">为什么BiliPlus上的分P列表和B站上的不一样？</div>
<div class="framedescription">为了提高访问速度和节省流量，BiliPlus会对获取的API数据进行缓存，访问时将读取缓存数据。<br/>如果BiliPlus的缓存数据过旧，请点击页面下方的“刷新数据”刷新缓存数据。</div>
<br/>
<hr>
<div class="framesubtitle">BiliPlus是怎样实现这些功能/获得这些数据的？</div>
<div class="framedescription">目前，BiliPlus中所有数据通过以下接口获得：<br/>·哔哩哔哩弹幕网开放平台(api.bilibili.com) [请参阅：<a href="http://docs.bilibili.cn/wiki/%E5%88%86%E7%B1%BB:API" target="_blank">分类:API - 嗶哩嗶哩百科</a>]<br/>·哔哩哔哩弹幕网内部接口(interface.bilibili.com) [请与 <a href="mailto:orz@loli.my" target="_blank">orz@loli.my</a> 联系]<br/>·哔哩哔哩唧唧ASS弹幕接口(非开放接口) [本接口为非开放接口，仅供哔哩哔哩唧唧及BiliPlus内部使用]</div>
